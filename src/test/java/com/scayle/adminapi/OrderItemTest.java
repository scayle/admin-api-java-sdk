package com.scayle.adminapi;

import com.scayle.adminapi.model.*;

import org.junit.Test;

import static net.javacrumbs.jsonunit.assertj.JsonAssertions.assertThatJson;
import static net.javacrumbs.jsonunit.core.Option.TREATING_NULL_AS_ABSENT;
import static org.junit.Assert.assertNotNull;
import static org.junit.Assert.assertTrue;

import java.util.Map;
import java.util.HashMap;
import java.util.List;

@SuppressWarnings("unchecked")
public class OrderItemTest extends BaseApiTest {

    @Test
    public void testCreateOrUpdateLegacyCustomData() throws Exception {
        String expectedRequestJson = this.loadFixture("/fixtures/OrderItemCreateOrUpdateLegacyCustomDataRequest.json");
        Map<String, Object> requestEntity = this.jsonSerializer.unserializeApiObject(expectedRequestJson, Map.class);

        assertThatJson(expectedRequestJson)
            .when(TREATING_NULL_AS_ABSENT)
            .isEqualTo(this.jsonSerializer.serializeApiObject(requestEntity));

        ApiOptions options = ApiOptions.builder().build();
        OrderItem responseEntity = this.api.orderItems().createOrUpdateLegacyCustomData("acme", "acme", 1, 1, requestEntity, options);

        String expectedResponseJson = this.loadFixture("/fixtures/OrderItemCreateOrUpdateLegacyCustomDataResponse.json");
        assertThatJson(expectedResponseJson)
            .when(TREATING_NULL_AS_ABSENT)
            .isEqualTo(this.jsonSerializer.serializeApiObject(responseEntity));


    }

}
