@SuppressWarnings("unchecked")
public class BulkRequestTest extends BaseApiTest {

    @Test
    public void testCancel() throws Exception {

        ApiOptions options = ApiOptions.builder().build();
        BulkRequestStatus responseEntity = this.api.bulkRequests().cancel(1, options);

        String expectedResponseJson = this.loadFixture("/fixtures/BulkRequestCancelResponse.json");
        assertThatJson(expectedResponseJson)
            .when(TREATING_NULL_AS_ABSENT)
            .isEqualTo(this.jsonSerializer.serializeApiObject(responseEntity));


    }

}
